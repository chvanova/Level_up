public class OperationAddition {

    static double addition(double num1, double num2){
        return num1 + num2;
    }
    static double addition(int num1, int num2){
        int result;
        result = num1 + num2;
        return (double) result;
    }
    static double addition(long num1, long num2){
        long result;
        result = num1 + num2;
        return (double) result;
    }
}
